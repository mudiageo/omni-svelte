describe('plugin_omni_virtual_aliases – resolveId', () => {
    it('resolves $models to virtual:omni:models', () => {
        expect(resolveId('$models')).toBe('\0virtual:omni:models');
    });

    it('resolves $schema to virtual:omni:schema', () => {
        expect(resolveId('$schema')).toBe('\0virtual:omni:schema');
    });

    it('resolves $validation to virtual:omni:validation', () => {
        expect(resolveId('$validation')).toBe('\0virtual:omni:validation');
    });

    it('resolves $db to virtual:omni:db', () => {
        expect(resolveId('$db')).toBe('\0virtual:omni:db');
    });

    it('passes through sub-path imports ($models/posts.model) unmodified', () => {
        expect(resolveId('$models/posts.model')).toBeUndefined();
    });
});

// ─────────────────────────────────────────────────────────────
describe('plugin_omni_virtual_aliases – load barrel generation', () => {
    const modelsRoot = `${ROOT}/src/lib/db/models`;
    const zodRoot = `${ROOT}/src/lib/db/validation`;
    const drizzleRoot = `${ROOT}/src/lib/db/server/schema.ts`;

    it('generates correct $db export', () => {
        const code = loadDb();
        expect(code).toContain("import { database } from 'omni-svelte/database'");
        expect(code).toContain("export { database as db }");
    });

    it('generates correct $schema re-export', () => {
        const code = loadSchema(drizzleRoot);
        expect(code).toContain(`export * from '${drizzleRoot.replace(/\\/g, '/')}'`);
    });

    it('generates per-schema barrel for $models', () => {
        const files = ['users.model.ts', 'posts.model.ts'];
        const code = loadModels(modelsRoot, files, 'per-schema');
        expect(code).toContain('users.model.ts');
        expect(code).toContain('posts.model.ts');
    });

    it('generates single-file re-export for $models', () => {
        const code = loadModels(modelsRoot, [], 'single-file');
        expect(code).toBe(`export * from '${modelsRoot}';`);
    });

    it('generates per-schema barrel for $validation', () => {
        const files = ['users.validation.ts', 'posts.validation.ts'];
        const code = loadValidation(zodRoot, files, 'per-schema');
        expect(code).toContain('users.validation.ts');
        expect(code).toContain('posts.validation.ts');
    });

    it('returns placeholder comment when models dir is empty', () => {
        const code = loadModels(modelsRoot, [], 'per-schema');
        expect(code).toContain('No model files found');
    });
});

// ─────────────────────────────────────────────────────────────
describe('plugin_omni_virtual_aliases – omni-env.d.ts smart diff', () => {
    it('generates valid d.ts content with correct module declarations', () => {
        const content = generateDtsContent(fakeConfig, ROOT);
        expect(content).toContain(`declare module '$models'`);
        expect(content).toContain(`declare module '$schema'`);
        expect(content).toContain(`declare module '$validation'`);
        expect(content).toContain(`declare module '$db'`);
    });

    it('uses $lib/... aliases — never relative ./ paths inside declare module blocks', () => {
        // TypeScript forbids relative imports inside ambient declare module declarations.
        // Every export * from '...' inside a declare module must be a non-relative specifier.
        const content = generateDtsContent(fakeConfig, ROOT);
        const declareModuleBlocks = content.match(/declare module '[^']+' \{[^}]+\}/gs) ?? [];
        for (const block of declareModuleBlocks) {
            const exportLines = block.match(/export \* from '[^']+'/g) ?? [];
            for (const line of exportLines) {
                expect(line).not.toMatch(/from '\.\//);
            }
        }
        // Positively assert $lib prefix is used for real paths
        expect(content).toContain(`from '$lib/`);
    });

    it('produces same output for same config (idempotent - would not trigger write)', () => {
        const first = generateDtsContent(fakeConfig, ROOT);
        const second = generateDtsContent(fakeConfig, ROOT);
        expect(first.trim()).toBe(second.trim());
    });
});
