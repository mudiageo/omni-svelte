import { describe, it, expect, vi, beforeEach } from 'vitest';
import { existsSync } from 'fs';
import { resolve } from 'path';

// Minimal fake omniConfig matching the schema output config
const fakeConfig = {
    schema: {
        output: {
            model: { path: 'src/lib/db/models', format: 'per-schema' as const },
            drizzle: { path: 'src/lib/db/server/schema.ts', format: 'single-file' as const },
            zod: { path: 'src/lib/db/validation', format: 'per-schema' as const },
        }
    }
};

const ROOT = 'C:/fake-project';

/**
 * Utility: simulate what resolveId does in the plugin
 */
const VIRTUAL_PREFIX = '\0virtual:omni:';
function resolveId(id: string): string | undefined {
    if (id === '$models' || id === '$schema' || id === '$validation' || id === '$db') {
        return VIRTUAL_PREFIX + id.slice(1);
    }
    // Sub-path: pass through to alias
    return undefined;
}

/**
 * Utility: simulate what load() does (simplified)
 */
function loadDb(): string {
    return `import { database } from 'omni-svelte/database';\nexport { database as db };\nexport default database;`;
}

function loadSchema(schemaPath: string): string {
    return `export * from '${schemaPath.replace(/\\/g, '/')}';`;
}

function loadModels(modelsPath: string, files: string[], format: 'per-schema' | 'single-file'): string {
    if (format === 'single-file') return `export * from '${modelsPath}';`;
    if (!files.length) return `// No model files found in ${modelsPath}`;
    return files.map(f => `export * from '${modelsPath}/${f}';`).join('\n');
}

function loadValidation(zodPath: string, files: string[], format: 'per-schema' | 'single-file'): string {
    if (format === 'single-file') return `export * from '${zodPath}';`;
    if (!files.length) return `// No validation files found in ${zodPath}`;
    return files.map(f => `export * from '${zodPath}/${f}';`).join('\n');
}

/**
 * Utility: simulate omni-env.d.ts content generation
 * Must match the real plugin's logic in packages/core/src/vite/index.ts.
 * Uses $lib/... aliases (non-relative) because TypeScript forbids relative
 * imports inside ambient `declare module` blocks.
 */
function generateDtsContent(config: typeof fakeConfig, root: string): string {
    const out = config.schema.output;
    const libDir = resolve(root, 'src/lib');

    const modelsAbsPath = resolve(root, out.model.path);
    const drizzleAbsPath = resolve(root, out.drizzle.path);
    const zodAbsPath = resolve(root, out.zod.path);

    const toLibAlias = (absPath: string) =>
        '$lib/' + absPath.replace(libDir, '').replace(/^[\\/]/, '').replace(/\\/g, '/').replace(/\.ts$/, '');

    const modelsAlias = toLibAlias(modelsAbsPath);
    const drizzleAlias = toLibAlias(drizzleAbsPath);
    const zodAlias = toLibAlias(zodAbsPath);

    const modelsIndex = out.model.format === 'single-file' ? modelsAlias : `${modelsAlias}/index`;
    const zodIndex = out.zod.format === 'single-file' ? zodAlias : `${zodAlias}/index`;

    return [
        `// Auto-generated by omni-svelte. Do not edit manually.`,
        `// Re-generated only when omniConfig.schema.output paths change.`,
        `// NOTE: ambient declare modules must use non-relative specifiers (TS rule).`,
        `//       All paths below use the $lib alias resolved by SvelteKit's tsconfig.`,
        ``,
        `// SERVER-ONLY: Models (contain database queries)`,
        `declare module '$models' { export * from '${modelsIndex}'; }`,
        `declare module '$models/*' { const mod: any; export = mod; }`,
        ``,
        `// SERVER-ONLY: Drizzle schema table definitions`,
        `declare module '$schema' { export * from '${drizzleAlias}'; }`,
        ``,
        `// SERVER-ONLY: Raw database connection`,
        `declare module '$db' {`,
        `  import type { PostgresJsDatabase } from 'drizzle-orm/postgres-js';`,
        `  export const db: PostgresJsDatabase;`,
        `}`,
        ``,
        `// UNIVERSAL: Zod validation schemas (safe for client and server)`,
        `declare module '$validation' { export * from '${zodIndex}'; }`,
        `declare module '$validation/*' { const mod: any; export = mod; }`,
    ].join('\n');
}

